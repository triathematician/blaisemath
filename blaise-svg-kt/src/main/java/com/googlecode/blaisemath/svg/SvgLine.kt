/**
 *
 *
 * Svg-compatible line.
 *
 * @author elisha
 */
@XmlRootElement(name = "line")
class SvgLine @JvmOverloads constructor(private var x1: Double = 0.0, private var y1: Double = 0.0, private var x2: Double = 0.0, private var y2: Double = 0.0) : SvgElement("line") {
    //<editor-fold defaultstate="collapsed" desc="PROPERTY PATTERNS">
    //
    // PROPERTY PATTERNS
    //
    @XmlAttribute
    fun getX1(): Double {
        return x1
    }

    fun setX1(x1: Double) {
        this.x1 = x1
    }

    @XmlAttribute
    fun getY1(): Double {
        return y1
    }

    fun setY1(y1: Double) {
        this.y1 = y1
    }

    @XmlAttribute
    fun getX2(): Double {
        return x2
    }

    fun setX2(x2: Double) {
        this.x2 = x2
    }

    @XmlAttribute
    fun getY2(): Double {
        return y2
    }

    fun setY2(y2: Double) {
        this.y2 = y2
    }

    private class LineConverter : Converter<SvgLine?, Line2D?>() {
        protected fun doBackward(r: Line2D?): SvgLine? {
            return SvgLine(r.getX1(), r.getY1(), r.getX2(), r.getY2())
        }

        protected fun doForward(r: SvgLine?): Line2D? {
            return Line2D.Double(r.x1, r.y1, r.x2, r.y2)
        }
    }

    companion object {
        private val CONVERTER_INST: LineConverter? = LineConverter()

        //</editor-fold>
        fun shapeConverter(): Converter<SvgLine?, Line2D?>? {
            return CONVERTER_INST
        }
    }
}