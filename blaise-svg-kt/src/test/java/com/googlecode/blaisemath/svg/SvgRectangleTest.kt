class SvgRectangleTest : TestCase() {
    @Test
    fun testConvertToSvg() {
        println("convertToSvg")
        val conv: Converter<SvgRectangle?, RectangularShape?> = SvgRectangle.Companion.shapeConverter()
        val r: Rectangle2D = Rectangle2D.Double(1.0, 2.0, 4.0, 3.0)
        val rect: SvgRectangle = conv.reverse().convert(r)!!
        assertEquals(1.0, rect.x, 1e-6)
        assertEquals(2.0, rect.y, 1e-6)
        assertEquals(4.0, rect.width, 1e-6)
        assertEquals(3.0, rect.height, 1e-6)
        assertEquals(0.0, rect.rx, 1e-6)
        assertEquals(0.0, rect.ry, 1e-6)
        val rr: RoundRectangle2D = RoundRectangle2D.Double(1.0, 2.0, 4.0, 3.0, .2, .3)
        val rrect: SvgRectangle = conv.reverse().convert(rr)!!
        assertEquals(1.0, rrect.x, 1e-6)
        assertEquals(2.0, rrect.y, 1e-6)
        assertEquals(4.0, rrect.width, 1e-6)
        assertEquals(3.0, rrect.height, 1e-6)
        assertEquals(.2, rrect.rx, 1e-6)
        assertEquals(.3, rrect.ry, 1e-6)
        assertEquals(null, conv.reverse().convert(null))
    }

    @Test
    fun testConvertFromSvg() {
        println("convertToSvg")
        val conv: Converter<SvgRectangle?, RectangularShape?> = SvgRectangle.Companion.shapeConverter()
        val sRect = SvgRectangle(1, 2, 3, 4)
        val rect: RectangularShape = conv.convert(sRect)!!
        assertTrue(rect is Rectangle2D.Double)
        assertEquals(1.0, rect.x, 1e-6)
        assertEquals(2.0, rect.y, 1e-6)
        assertEquals(3.0, rect.width, 1e-6)
        assertEquals(4.0, rect.height, 1e-6)
        val sRect2 = SvgRectangle(1, 2, 3, 4, .2, .3)
        val rect2: RectangularShape = conv.convert(sRect2)!!
        assertTrue(rect2 is RoundRectangle2D.Double)
        assertEquals(1.0, rect2.x, 1e-6)
        assertEquals(2.0, rect2.y, 1e-6)
        assertEquals(3.0, rect2.width, 1e-6)
        assertEquals(4.0, rect2.height, 1e-6)
        assertEquals(.2, (rect2 as RoundRectangle2D).arcWidth)
        assertEquals(.3, (rect2 as RoundRectangle2D).arcHeight)
        assertEquals(null, conv.convert(null))
    }
}